#For loop
i = ['Item 1', 'Item 2', 'Item 3', 'Item 4', 'Item 5']
for values in i:
    print(values)

#Range
print()
for i in range(1,10):
    print(i)

#Search string in For loop
print()
for i in "Felix ITs":
    print(i)

#Nested Loops
i = ["Item 1", "Item 2", "Item 3"]
j = ["HTML", "CSS", "Bootstrap"]
print()
for x in i:
    for y in j:
        print(x, y)
